import os
from app.config import get_settings

settings = get_settings()


def ensure_upload_dir(module: str = "papers") -> str:
    path = os.path.join(settings.upload_dir, module)
    os.makedirs(path, exist_ok=True)
    return path



def count_tokens_approx(text: str) -> int:
    return len(text) // 3


def build_summarize_prompt(paper_content: str) -> str:
    return f"""你是一名面向CAE论文的工程论文总结助手。请阅读以下论文内容，围绕“问题、结论、工况”三个方面进行精炼的结构化总结。

总结要求：

* 只基于论文原文内容，不要编造论文中没有出现的信息。
* **重点标粗**：对以下内容使用 Markdown 加粗语法 `**关键内容**`：工程方法（风洞实验、CFD仿真等）、研究对象（AGS、散热器等）、核心数值（百分比、系数等）、关键指标（效率、阻力等）。每段至少标粗2-3处。
* 优先保留关键工程对象、设计变量、仿真/实验条件、评价指标和数值结果。
* 如果论文中没有明确给出某项内容，请写“文中未明确给出”，不要猜测。
* 输出使用 Markdown，严格按照以下格式。

## 解决的问题

用一句话概括论文研究的核心工程问题，控制在30-45字之间。

要求：

优先采用“研究方法 + 研究对象 + 核心问题/指标”的句式；
尽量保留论文采用的方法，例如风洞实验、CFD仿真、有限元分析、优化设计等；
必须包含研究对象，例如车辆、叶片、换热器、结构件、流场、热管理系统等；
必须包含核心关注指标或问题，例如阻力、升力、温度、压力损失、应力、变形、效率、误差等；
不展开研究背景、研究意义和复杂目的；
避免使用“旨在表征并理解”“系统研究了”“深入分析了”等偏摘要化表达；
不要把多个设计变量、多个研究目的全部写进去，只保留最核心问题。

推荐句式：
该文献通过[方法]研究[对象]的[核心问题/指标]。

示例：
该文献通过风洞实验研究林肯MKZ冷却阻力来源与AGS效率影响因素。

## 结论

总结论文最核心的3-5条发现。

要求：
- 每条控制在25-40字之间，尽量接近30字；
- 每条只表达一个核心发现；
- 采用“对象 + 关键变化 + 核心结果/影响”的句式；
- 优先保留最关键的数值，但每条最多保留1个核心数值；
- 不展开机理解释、背景说明和复杂原因分析；
- 避免过短判断句，例如“效果明显”“阻力更低”；
- 避免过长复合句，例如同时写多个现象、多个原因、多个指标。

输出示例：
1. AGS侧密封度由0%提高到100%时，AGS效率约提升15%。
2. 下AGS叶片靠近格栅布置时，AGS效率提高且影响车底气流。
3. 发动机下护板存在时，散热器流阻和舱内附件影响更明显。

## 工况

从论文的实验、仿真、优化或参数对比章节中，提取最具代表性的工况，形成表格。

选择规则：

* 每个独立实验/仿真/优化/参数分析章节只选1个工况，最多不超过2个；
* 优先选择与论文核心结论直接相关的工况；
* 优先选择效果最优、改善最明显、误差最小、影响最大或作者重点讨论的工况；
* 如果该章节不是优化研究，没有明确最优工况，则选择最能代表该章节结论的“代表性工况”；
* 如果某个工况导致性能明显变差，但对论文结论很重要，可以标记为“关键不利工况”；
* 不要罗列全部工况；
* 不要把多个对比工况、多个参数范围或多个实验现象塞进同一行；
* 表头固定为：章节、工况类型、研究对象、关键工况参数、对比基准、关键指标、效果；
* “关键工况参数”只写该工况最核心的参数，控制在30字以内；
* “对比基准”优先填写论文中的基准工况、原始方案或对照组；只有完全无法判断时才写“文中未明确给出”；
* “效果”只写关键指标变化，控制在30字以内；
* 不解释原因，不展开机理，不重复结论部分内容；
* 如果参数或效果文中没有明确给出，填写“文中未明确给出”，不要猜测。

表格格式：

| 章节        | 工况类型   | 研究对象   | 关键工况参数          | 对比基准      | 关键指标  | 效果              |
| --------- | ------ | ------ | --------------- | --------- | ----- | --------------- |
| AGS侧密封影响  | 最优工况   | AGS侧密封 | 侧密封度100%        | 无侧密封      | AGS效率 | 效率提升约15%，最高约55% |
| AGS叶片位置影响 | 最优工况   | 下AGS叶片 | 距格栅65mm以内，80MPH | 冷凝器正前方0mm | AGS效率 | AGS效率明显提高       |
| 底部护板研究    | 关键不利工况 | 中央隧道护板 | 移除中央隧道护板        | 全套平底护板    | 冷却阻力  | 冷却阻力明显上升        |
---
论文内容：

{paper_content}
"""



